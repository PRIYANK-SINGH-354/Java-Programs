// 1. Creating my first class and performing basic arithmetic operations on two variables
public class FirstClass {
    public static void main(String[] args) {
        int a=12, b=5;
        int c= a+b;
        int d= a-b;
        int e= a*b;
        int f= a/b;
        int g= a%b;
        System.out.println("Sum of the numbers is: " + c);
        System.out.println("Difference of the number is: " + d);
        System.out.println("Multiplication of the number is: " + e);
        System.out.println("Division of the number is: " + f);
        System.out.println("Remainder when a is divided by b is: " + g);
    }
}
